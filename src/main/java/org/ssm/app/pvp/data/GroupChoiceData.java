package org.ssm.app.pvp.data;

public class GroupChoiceData {

	private String groupName;
	private String groupShortName;
	private Integer schoolsCount;
	private Integer totalRegistredStudents;
	
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getGroupShortName() {
		return groupShortName;
	}
	public void setGroupShortName(String groupShortName) {
		this.groupShortName = groupShortName;
	}
	public Integer getSchoolsCount() {
		return schoolsCount;
	}
	public void setSchoolsCount(Integer schoolsCount) {
		this.schoolsCount = schoolsCount;
	}
	public Integer getTotalRegistredStudents() {
		return totalRegistredStudents;
	}
	public void setTotalRegistredStudents(Integer totalRegistredStudents) {
		this.totalRegistredStudents = totalRegistredStudents;
	}
	
	
}
